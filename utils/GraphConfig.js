/*
非年报数据特殊情况使用方法：
1、splitByTime为false的类型：
·日期类型为字符串，可以直接进行展示的，则什么都不用管
·日期类型为字符串，想在日期字符后面加上星期几的，则intervalConvert标识为stringToWeek
·日期类型为时间戳，转换为分钟/小时/天，则添加invervalConvert标识minutes/hours/date
·返回数据category1不固定，则添加标识notFilter为true
2、splitByTime为true的类型：
·日期类型为字符串：什么都不用管
·日期类型为时间戳：按天展示，则interval为date；按年展示则interval为year
·返回数据category1不固定，则添加标识notFilter为true
3、chartHidden:
.该指标使用于每个指标的chart中的每个配置中，当为true时在手机端不显示对应图，
.为false或者不设置时在手机端显示对应图
4、dateSort
.该参数用于设置有时间顺序的表格数据的显示顺序，当为true时，表格数据按照时间顺序排列，
.当为false或者不设置时，表格数据逆序显示
*/
/*
小数位数处理规范（参数：decimalPlaces）
·该项指标全部数据都是整数，则不加decimalPlaces参数
·该项指标全部数据都是统一位数的小数，则decimalPlaces对象添加key值为all，value值为小数的位数
·该项指标不同category数据的小数位数不统一的情况，如该category数据都是整数，则不做处理，如果是小数，
则在decimalPlaces对象中添加key值为该category，value为对应的小数位数
 */
/*
图表展示规范
图表的y轴最大值最小值，正常是根据数据计算出来的，部分指标可以通过配置来确定y轴的最大值和最小值；
·colume-line图表类型：左侧最大值标识为：maxYLeft，最小值标识为minYLeft；右侧最大值标识为：maxYRight，最小值标识为minYRight
·非colume-line图表类型：其中最大值标识为：maxY，最小值标识为minY
*/
